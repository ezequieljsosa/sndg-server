package ar.com.bia.entity;

public class AssemblyDoc {

	private String submitter;
	private String date;
	private String coverage;
	private String align_error;
	private String n_count_scaffolds;
	private String n_count_contigs;
	private String scaffolds;
	private String n50;
	
	public String getSubmitter() {
		return submitter;
	}
	public void setSubmitter(String submitter) {
		this.submitter = submitter;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getCoverage() {
		return coverage;
	}
	public void setCoverage(String coverage) {
		this.coverage = coverage;
	}
	public String getAlign_error() {
		return align_error;
	}
	public void setAlign_error(String align_error) {
		this.align_error = align_error;
	}
	public String getN_count_scaffolds() {
		return n_count_scaffolds;
	}
	public void setN_count_scaffolds(String n_count_scaffolds) {
		this.n_count_scaffolds = n_count_scaffolds;
	}
	public String getN_count_contigs() {
		return n_count_contigs;
	}
	public void setN_count_contigs(String n_count_contigs) {
		this.n_count_contigs = n_count_contigs;
	}
	public String getScaffolds() {
		return scaffolds;
	}
	public void setScaffolds(String scaffolds) {
		this.scaffolds = scaffolds;
	}
	public String getN50() {
		return n50;
	}
	public void setN50(String n50) {
		this.n50 = n50;
	}

	
	
	
	
}
